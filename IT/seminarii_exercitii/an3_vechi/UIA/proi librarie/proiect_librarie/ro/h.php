		<h2 align="center">Instructiuni pentru cumparare</h2>
		<h3>(aceasta pagina ofera toate detaliile si pasii pentru cumpararea produselor)</h3>
		<h5>Pasii ce trebuiesc parcursi pentru cumpararea unei carti:</h5>
		<p>1.pentru achizitionarea produsului dorit, cumparatorul alege unul din domenii. Cu un singur click pe domeniul dorit,o sa va apara o lista cu cartile care sunt in stoc.(exemplu: daca se da un click pe beletristica, o sa va apara urmatoarele carti:"Ritmuri interioare", "London Fields", "Shutter Island")<br>
2.pentru adaugarea cartii dorite in cosul de cumparaturi, cumparatorul trebuie sa dea click pe carte,si i se va deschide o pagina unde are posibilitatea sa cumpere cartea(se apasa butonul "cumpara acum!". La apasarea butonului "cumpara acum!" o sa apara cosul de cumparaturi in care o sa fie trecute toate cartile pentru care s-a optat.)</p>
<p>Nota:<br> 
         - dupa introducerea unei carti in cosul de cumparaturi, cumparatorul poate opta intre: "continua cumparaturile" (si se repet pasii de mai sus) sau "mergi la casa"(pentru achizitionarea cartilor din cosul de cumparaturi, cumparatorul trebuie sa completeze cele doua campuri cu datele corecte)</p>

<h5>Modificari in Cosul de cumparturi:</h5>
<p>(in cazul in care cumparatorul doreste sa modifice numarul de bucati al unui produs sau sa il elimine din cosul de cumparaturi)</p>
<p>- daca se doreste achizitionarea mai multor bucati a unei carti,cumparatorul trebuie sa mearga la cosul de cumparaturi, plaseaza cursorul mouse-ului in casuta in care apare numarul de bucati si sa modifice valoarea respectiva, dupa care se apasa butonul modifica.</p>
<p>-in cazul in care cumparatorul doreste sa elimine un produs din cosul de cumparaturi,acesta trebuie sa treaca valoarea "0" la numarul de bucati din dreptul respectivului produs si sa apese butonul modifica. Dupa apasarea butonului modifica, produsul va fi sters din cos.</p>

